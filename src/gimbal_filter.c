/**
  * @fn     filter_ema
  * @brief  
  *
  * @param p_pre_val  
  * @param p_val      
  */
void filter_ema(float* p_val)
{
  p_val[1] = (p_val[0] * EMA_K) + (p_val[1] * (1 - EMA_K));
  p_val[0] = p_val[1];
}

